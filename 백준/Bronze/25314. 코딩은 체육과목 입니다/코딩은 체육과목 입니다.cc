#include <bits/stdc++.h>

using namespace std;

int main(){

        ios_base::sync_with_stdio(false);
        cin.tie(0);

        int N;

        cin >> N;

        int result = N / 4;

        for(register int i = 0; i < result - 1; i++){
                cout << "long ";
        }

        cout << "long int\n";


        return 0;

}
